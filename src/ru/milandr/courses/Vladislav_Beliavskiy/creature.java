package ru.milandr.courses.Vladislav_Beliavskiy;

public class creature {
    private int NumberOfLegs;
    private int NumberOfEyes;
    private int NumberOfEars;
    private int Age;
    private String name;

    public int getNumberOfLegs ()
    {
        return this.NumberOfLegs;
    }

    public void setNumberOfLegs (int NumberOfLegs)
    {
        this.NumberOfLegs = NumberOfLegs;
    }

    public int getNumberOfEyes ()
    {
        return this.NumberOfEyes;
    }

    public void setNumberOfEyes (int NumberOfEyes)
    {
        this.NumberOfEyes = NumberOfEyes;
    }

    public int getNumberOfEars ()
    {
        return this.NumberOfEars;
    }

    public void setNumberOfEars (int NumberOfEars)
    {
        this.NumberOfEars = NumberOfEars;
    }

    public int getAge ()
    {
        return this.Age;
    }

    public void setAge (int Age)
    {
        this.Age = Age;
    }

    public String getName ()
    {
        return this.name;
    }

    public void setName (String name)
    {
        this.name = name;
    }

}
